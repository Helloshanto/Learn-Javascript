// JavaScript Assignment Operators
/**
 * =   => a=b 
 * +=  => 
 * -=
 * *=
 * /=
 * %=
 * **=
*/

var a = 20;
var b = 10;
console.log(a + b)

// +=
a += b; //=> a=a+b
console.log(a)

//-=
a -= b;;
console.log(a);

//-=
a *= b;;
console.log(a);

// /=
a /= b;
console.log(a);


// %=
a %= b;
console.log(a);


// **=

var x = 2;
var y = 5;

x **= y;
console.log(x);


